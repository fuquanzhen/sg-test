package com.sixe;

public class NewQuestion3 {
	private float sum;
	private float avg;
	private float z;
	private int max;
	private int min;

	public NewQuestion3(float sum, float avg, float z, int max, int min) {
		this.sum = sum;
		this.avg = avg;
		this.z = z;
		this.max = max;
		this.min = min;

	}

	public float getSum() {
		return sum;
	}

	public float getAvg() {
		return avg; 
	}

	public float getZ() {
		return z;
	}

	public int getMax() {
		return max;
	}

	public int getMin() {
		return min;
	}
}
